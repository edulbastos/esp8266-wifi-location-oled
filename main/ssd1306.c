static esp_err_t ssd1306_write_command(uint8_t cmd)
{
    i2c_cmd_handle_t i2c_cmd = i2c_cmd_link_create();
    i2c_master_start(i2c_cmd);
    i2c_master_write_byte(i2c_cmd, (SSD1306_I2C_ADDR << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(i2c_cmd, 0x00, true); // comando
    i2c_master_write_byte(i2c_cmd, cmd, true);
    i2c_master_stop(i2c_cmd);
    esp_err_t ret = i2c_master_cmd_begin(I2C_MASTER_NUM, i2c_cmd, pdMS_TO_TICKS(I2C_MASTER_TIMEOUT_MS));
    i2c_cmd_link_delete(i2c_cmd);
    return ret;
}

static esp_err_t ssd1306_write_data(uint8_t *data, size_t len)
{
    i2c_cmd_handle_t i2c_cmd = i2c_cmd_link_create();
    i2c_master_start(i2c_cmd);
    i2c_master_write_byte(i2c_cmd, (SSD1306_I2C_ADDR << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(i2c_cmd, 0x40, true); // dados
    i2c_master_write(i2c_cmd, data, len, true);
    i2c_master_stop(i2c_cmd);
    esp_err_t ret = i2c_master_cmd_begin(I2C_MASTER_NUM, i2c_cmd, pdMS_TO_TICKS(I2C_MASTER_TIMEOUT_MS));
    i2c_cmd_link_delete(i2c_cmd);
    return ret;
}

esp_err_t ssd1306_init(int sda_pin, int scl_pin)
{
    i2c_config_t conf = {
        .mode = I2C_MODE_MASTER,
        .sda_io_num = sda_pin,
        .scl_io_num = scl_pin,
        .sda_pullup_en = GPIO_PULLUP_ENABLE,
        .scl_pullup_en = GPIO_PULLUP_ENABLE,
    };

    esp_err_t ret = i2c_driver_install(I2C_MASTER_NUM, conf.mode);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "I2C driver install failed");
        return ret;
    }

    ret = i2c_param_config(I2C_MASTER_NUM, &conf);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "I2C param config failed");
        return ret;
    }
 

    vTaskDelay(pdMS_TO_TICKS(100));

    // Sequência de inicialização do SSD1306
    ssd1306_write_command(SSD1306_CMD_DISPLAY_OFF);
    ssd1306_write_command(SSD1306_CMD_SET_MUX_RATIO);
    ssd1306_write_command(0x3F);
    ssd1306_write_command(SSD1306_CMD_SET_DISPLAY_OFFSET);
    ssd1306_write_command(0x00);
    ssd1306_write_command(SSD1306_CMD_SET_START_LINE | 0x00);
    ssd1306_write_command(SSD1306_CMD_SET_SEGMENT_REMAP);
    ssd1306_write_command(SSD1306_CMD_SET_COM_SCAN_DEC);
    ssd1306_write_command(SSD1306_CMD_SET_COM_PINS);
    ssd1306_write_command(0x12);
    ssd1306_write_command(SSD1306_CMD_SET_CONTRAST);
    ssd1306_write_command(0x7F);
    ssd1306_write_command(SSD1306_CMD_NORMAL_DISPLAY);
    ssd1306_write_command(SSD1306_CMD_CHARGE_PUMP);
    ssd1306_write_command(0x14);
    ssd1306_write_command(SSD1306_CMD_MEMORY_MODE);
    ssd1306_write_command(0x00);
    ssd1306_write_command(SSD1306_CMD_DISPLAY_ON);

    ssd1306_clear();
    ssd1306_display();

    ESP_LOGI(TAG, "SSD1306 initialized");
    return ESP_OK;
}

void ssd1306_clear(void)
{
    memset(display_buffer, 0, sizeof(display_buffer));
}

void ssd1306_display(void)
{
    ssd1306_write_command(SSD1306_CMD_COLUMN_ADDR);
    ssd1306_write_command(0);
    ssd1306_write_command(SSD1306_WIDTH - 1);

    ssd1306_write_command(SSD1306_CMD_PAGE_ADDR);
    ssd1306_write_command(0);
    ssd1306_write_command((SSD1306_HEIGHT / 8) - 1);

    ssd1306_write_data(display_buffer, sizeof(display_buffer));
}

void ssd1306_set_pixel(int x, int y, bool on)
{
    if (x < 0 || x >= SSD1306_WIDTH || y < 0 || y >= SSD1306_HEIGHT) {
        return;
    }

    int byte_index = x + (y / 8) * SSD1306_WIDTH;
    int bit_index = y % 8;

    if (on) {
        display_buffer[byte_index] |= (1 << bit_index);
    } else {
        display_buffer[byte_index] &= ~(1 << bit_index);
    }
}

void ssd1306_draw_char(int x, int y, char c)
{
    if (c < ' ' || c > '~') {
        c = ' ';
    }

    const uint8_t *glyph = font_5x7[c - ' '];

    for (int i = 0; i < 5; i++) {
        uint8_t line = glyph[i];
        for (int j = 0; j < 7; j++) {
            if (line & (1 << j)) {
                ssd1306_set_pixel(x + i, y + j, true);
            }
        }
    }
}

void ssd1306_draw_string(int x, int y, const char *str)
{
    int cur_x = x;

    while (*str) {
        ssd1306_draw_char(cur_x, y, *str);
        cur_x += 6; // 5 pixels de largura + 1 de espaço
        str++;
    }
}
